    private void tablaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaMouseClicked
        int fila = tabla.getSelectedRow();

        id = Integer.parseInt(tabla.getValueAt(fila, 0).toString());
        String nom = tabla.getValueAt(fila, 1).toString();
        String precio = tabla.getValueAt(fila, 2).toString();
        String stock = tabla.getValueAt(fila, 3).toString();
        String es = tabla.getValueAt(fila, 4).toString();
        txtNombre.setText(nom);
        txtPrecio.setText(precio);
        txtStock.setText(stock);
        comboEstado.setSelectedItem(es);

    }//GEN-LAST:event_tablaMouseClicked
    public void agregar(){
        String nom = txtNombre.getText();
        String precio = txtPrecio.getText();
        String stock = txtStock.getText();
        String es = comboEstado.getSelectedItem().toString();
        Object[] ob = new Object[4];
        ob[0] = nom;
        ob[1] = precio;
        ob[2] = stock;
        ob[3] = es;
        dao.add(ob);
    }
    public void actualizar(){
        int fila = tabla.getSelectedRow();
        if(fila == -1){
            JOptionPane.showMessageDialog(this, "Debe seleccionar una fila.");
        } else {
            String nom = txtNombre.getText();
            String precio = txtPrecio.getText();
            String stock = txtStock.getText();
            String es = comboEstado.getSelectedItem().toString();
            Object[] ob = new Object[5];
            ob[0] = nom;
            ob[1] = precio;
            ob[2] = stock;
            ob[3] = es;
            ob[4] = id;
            
            dao.actualizar(ob);
        }
        
    }
    public void eliminar(){
        int fila = tabla.getSelectedRow();
        if(fila == -1){
            JOptionPane.showMessageDialog(this, "Debe seleccionar una fila");
        } else {
            dao.eliminar(id);
        }
    }
    public void nuevo(){
        txtNombre.setText("");
        txtPrecio.setText("");
        txtStock.setText("");
        comboEstado.setSelectedItem(0);
        txtNombre.requestFocus();
    }
    public void limpiarTabla(){
        for(int i = 0; i < modelo.getRowCount(); i++){
            modelo.removeRow(i);
            i = i - 1;
        }
    }

